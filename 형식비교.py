# 비교 및 장단점 설명
print("List:")
print(" - 데이터를 순서대로 저장하며 중복을 허용합니다.")
print(" - 수정 가능하고, 인덱스를 사용하여 원소에 접근 가능합니다.")
print(" - 자료의 추가/삭제가 자주 발생할 때 유용합니다.")

print("\nTuple:")
print(" - 데이터를 순서대로 저장하며 중복을 허용합니다.")
print(" - 수정 불가능하고, 읽기 전용 데이터 구조입니다.")
print(" - 보다 안전하고 빠르며, 데이터 변경이 필요 없는 경우에 사용됩니다.")

print("\nSet:")
print(" - 데이터를 순서 없이 저장하며, 중복을 허용하지 않습니다.")
print(" - 집합 연산(교집합, 합집합, 차집합 등)에 유용하고, 중복된 데이터 제거에 효과적입니다.")
print(" - 데이터의 존재 여부를 확인하는 데 매우 효율적입니다.")

print("\nDictionary:")
print(" - 키-값 쌍으로 데이터를 저장합니다.")
print(" - 검색 및 데이터 수정에 효과적이며, 해시 테이블을 사용하여 빠른 속도를 제공합니다.")
print(" - 데이터를 효율적으로 관리할 때 유용합니다.")
